export function formatCurrency(amount: number): string {
  return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', maximumFractionDigits: 0 }).format(amount);
}

export function formatNumber(n: number): string {
  return new Intl.NumberFormat('en-US').format(n);
}

export function getTerritoryBySlug(slug: string): Territory | undefined {
  return territories.find((t) => t.territory_slug === slug);
}

export function getTerritoriesByState(state: string): Territory[] {
  return territories.filter((t) => t.state === state || t.state_abbr === state);
}
